import java.awt.Point;

class PointSetter2
{
	public static void main(String[] args)
	{
		Point location = new Point(4,13);
		
		System.out.println(" point x, y  " + location.x + location.y);
		
		
		location.x =7;
		location.y= 13;
		System.out.println(" point x, y  " + location.x + location.y);
	}
	
	
	
	
	
	
	
}